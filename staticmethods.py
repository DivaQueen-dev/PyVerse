class Aditya:
    @staticmethod
    def add(x,y):
        print("The sum value is:",(x+y))
    @staticmethod
    def sub(x,y):
        print("The substraction value is:",(x-y))
    @staticmethod
    def avg(x,y):
        print("The Average value is :",(x+y)/2)
Aditya.add(200,100)
Aditya.sub(200,50)
Aditya.avg(200,100)
